package org.mapstruct.ap.test.builder.immutables;

/**
 * Mutable source DTO mapped to the immutable {@link Person} interface.
 */
public class PersonDto {

    private String name;
    private int age;

    public PersonDto(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
